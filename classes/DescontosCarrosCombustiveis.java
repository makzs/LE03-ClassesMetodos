package classes;

public class DescontosCarrosCombustiveis {
    public static double descontoA(double preco){
        return (preco * 25) / 100;
    
    }

    public static double descontoG(double preco){
        return (preco * 21) / 100;

    }

    public static double descontoD(double preco){
        return (preco * 14) / 100;

    }
}
